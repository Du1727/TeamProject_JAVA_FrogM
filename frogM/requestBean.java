package frogM;

public class requestBean {
	private String code;
	private String id;
	private String req_dt;
	private String want_dt;
	private String req_reason;
	private String req_status;
	
	
	public String getcode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	
	public String getReq_dt() {
		return req_dt;
	}
	public void setReq_dt(String req_dt) {
		this.req_dt = req_dt;
	}
	public String getWant_dt() {
		return want_dt;
	}
	public void setWant_dt(String want_dt) {
		this.want_dt = want_dt;
	}
	public String getReq_reason() {
		return req_reason;
	}
	public void setReq_reason(String req_reason) {
		this.req_reason = req_reason;
	}
	public String getReq_status() {
		return req_status;
	}
	public void setReq_status(String req_status) {
		this.req_status = req_status;
	}
}
