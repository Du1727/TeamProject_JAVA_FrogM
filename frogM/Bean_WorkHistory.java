package frogM;

public class Bean_WorkHistory {

	
	private String code;
	private String id;
	private String store_schedule_code;
	
	private String name;
	private String grade;
	
	
	
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getStore_schedule_code() {
		return store_schedule_code;
	}
	public void setStore_schedule_code(String store_schedule_code) {
		this.store_schedule_code = store_schedule_code;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getGrade() {
		return grade;
	}
	public void setGrade(String grade) {
		this.grade = grade;
	}
	
	
	
	
	
	
	
	
	
}
